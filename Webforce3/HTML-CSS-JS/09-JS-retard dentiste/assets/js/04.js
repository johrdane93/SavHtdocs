/*----------------------------------------
        ADDITION +
----------------------------------------*/

// -- Déclaration de plusieurs variable à la suite
var nb1, nb2, resultat;

nb1 = 10;
nb2 = 5;

// -- Addition de nb1 + nb2 avec l'opérateur "+"
resultat = nb1 + nb2;

// -- Affichage du résultat dans la console
console.log(resultat)
console.log("Le Resultat de l'Addition " + nb1 + " par " + nb2 + " est égal à : " + resultat);
/*----------------------------------------
        SOUSTRACTION -
----------------------------------------*/

// -- Soustraction de nb1 + nb2 avec l'opérateur "-"
resultat = nb1 - nb2;

// -- Affichage du résultat dans la console
console.log(resultat)
console.log("Le Resultat de la Soustraction " + nb1 + " par " + nb2 + " est égal à : " + resultat);

/*----------------------------------------
        MULTIPLICATION *
----------------------------------------*/

// -- Multiplication de nb1 + nb2 avec l'opérateur "*"
resultat = nb1 * nb2;

// -- Affichage du résultat dans la console
console.log(resultat)
console.log("Le Resultat de la Multiplication " + nb1 + " par " + nb2 + " est égal à : " + resultat);

/*----------------------------------------
        DIVISION /
----------------------------------------*/

// -- Division de nb1 + nb2 avec l'opérateur "/"
resultat = nb1 / nb2;

// -- Affichage du résultat dans la console
console.log(resultat)
console.log("Le Resultat de la Division " + nb1 + " par " + nb2 + " est égal à : " + resultat);

/*----------------------------------------
        MODULO %
----------------------------------------*/

// -- Division de nb1 + nb2 avec l'opérateur "%"
resultat = nb1 % nb2;

// -- Affichage du résultat dans la console
console.log(resultat)
console.log("Le Reste de la Division de " + nb1 + " par " + nb2 + " est égal à : " + resultat);

