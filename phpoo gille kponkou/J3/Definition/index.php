/*
 * Langage PHP et la programmation Orientée Objet
 * Beaucoup de langage implémente la POO, comme C ou le langage PHP.
 * PHP est un langage Impératif disposant de la version 5 
 * de fonctionnalités de modéle Objet
 * complétes .
 * 
 * 
 * Qu'est -ce Qu'un objet?
 * Un objet est un conteneur symbolique, qui posséde sa propre existence 
 * et englobe des caractéristique,état et comportement qui représente
 * quelque chose de tangible du monde réel.
 * 
 * en programmation orienté objet un objet
 * est créé à partir d'un modéle appelé classe, 
 * duquel il hérite les comportement et les caractéristique.
 * 
 * 
 * 
 * 
 * Q'est-Cs qu'une Classe?
 * Les classes sont présente pour fabriquer des objets.
 * En programmation orienté objet, un objet est créé sur 
 * le modéle de la classe à laquelle il appartient.
 * 
 * exemple : les gateaux et leur moule.
 * dans ce cas , les gâteaux sont les objets et le moule est la classe.
 * la classe est présente pour produir  des objets. 
 * elle contient le plan de fabrication d'un objet
 *  et nous pouvons nous en servir autant que nous le souhaitons 
 * afins d'obtenir une infinité d'objet.
 * 
 * Vocabulaire: En programmation objet, nous ne parlerons plus 
 * de fonctions mais méthode, nous ne parlerons plus de variables 
 * mais de'attribut ou de propriété.
 * 
 * Une classe est une entité regroupant
 * un certain nombre d'attribut et de méthode que tout objet issu
 *  de cette classe possédera.
 * 
 * 
 * Différence entre Une classe et un Objet
 * exemple: Plan de construction d'une voiture 
 * classe c'et le plan
 * objet c'est la voiture
 * 
 * Définition:
 * Une instance de classe c'est le fait de créer un objet de cette classe.
 * 
 * 
 * Création d'objet:
 * la création d'objet per plusieur choses:
 * - la création d'un espace mémoire
 * - l'attribution d'un nom à l'objet
 * - si l'objet posséde un constructeur, cet objet sera 
 * initialisé(avec des valeurs par défaut ou des valeurs passées 
 * en paramétre)
 * 
 * Instanciation
 * "new" peut être traduit comme " fabrique - moi un nouvel objet 
 * émanant de cette classe".
 * 
 * Quand vous instanciez une  class, la variable stockant 
 * l'objet ne stock pas l'objet lui-même
 *  mais un identifiant qui représente cet objet. 
 * 
 * Exemple $objet = new Maclasse;
 * 
